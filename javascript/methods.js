//Mathematical Methods:

//1-Math.pow(2,3); -> üzeri alır(exponential number) ->exponent(root)
var sayi0 = Math.pow(3,3);
console.log("Math.pow(3,3) methodu : "+sayi0);

//2-Math.sqrt(100) -> square root alır
var sayi1 = Math.sqrt(100);
console.log("Math.sqrt(100) methodu : "+sayi1);

//3-Math.abs(-12); -> absolute(mutlak)
var sayi2 = Math.abs(-12);
console.log("Math.abs(-12) methodu : "+sayi2);

//4-Math.floor(5,9); -> zemine,tabana çeker
var sayi3 = Math.floor(5.9);
console.log("Math.floor(5.9) methodu : "+sayi3);

//5-Math.ceil(5,9); -> tavana çeker
var sayi4 = Math.ceil(5.9);
console.log("Math.ceil(5.9) methodu : "+sayi4);

//6-Math.round(6.4); -> virgül sonrası 5+ ise bir üst tam sayıya,altı ise taban sayıya yuvarlar.
var sayi5 = Math.round(7.4);
console.log("Math.round(7.4) methodu : "+sayi5);

var sayi5 = Math.round(7.5);
console.log("Math.round(7.5) methodu : "+sayi5);

